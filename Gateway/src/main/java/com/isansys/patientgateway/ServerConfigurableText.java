package com.isansys.patientgateway;

import com.isansys.common.enums.ServerConfigurableTextStringTypes;

public class ServerConfigurableText
{
    private int servers_id;

    private ServerConfigurableTextStringTypes string_type;

    private String string_text;

    public int getServersId()
    {
        return servers_id;
    }

    public void setServersId(int servers_id)
    {
        this.servers_id = servers_id;
    }

    public ServerConfigurableTextStringTypes getStringType()
    {
        return string_type;
    }

    public void setStringType(ServerConfigurableTextStringTypes string_type)
    {
        this.string_type = string_type;
    }

    public String getStringText()
    {
        return string_text;
    }

    public void setStringText(String string)
    {
        this.string_text = string;
    }


    public ServerConfigurableText(int servers_id, ServerConfigurableTextStringTypes string_type, String string_text)
    {
        this.servers_id = servers_id;
        this.string_type = string_type;
        this.string_text = string_text;
    }
}
