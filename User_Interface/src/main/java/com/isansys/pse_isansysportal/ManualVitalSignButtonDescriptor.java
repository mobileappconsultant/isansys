package com.isansys.pse_isansysportal;


import android.graphics.drawable.Drawable;

class ManualVitalSignButtonDescriptor
{
    public final int button_id;

    public final String button_text;
    public final String button_rhs_description;
    public final int vital_sign_type_id;

    public final int ews_score;

    public final int button_text_colour;
    public final Drawable button_drawable;


    public ManualVitalSignButtonDescriptor(String button_text,
                                           int vital_sign_type_id,
                                           int button_id,

                                           int ews_score,

                                           int button_text_colour,
                                           Drawable button_drawable,

                                           String button_rhs_description)
    {
        this.button_text = button_text;
        this.vital_sign_type_id = vital_sign_type_id;
        this.button_id = button_id;
        this.ews_score = ews_score;

        this.button_text_colour = button_text_colour;
        this.button_drawable = button_drawable;

        this.button_rhs_description = button_rhs_description;
    }
}
